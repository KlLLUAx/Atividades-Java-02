package br.com.fuctura.entidades;

public class Coordenador {
protected String nomeCoordenador;
private int idCoordenadro;
protected String curso;
public String getNomeCoordenador() {
	return nomeCoordenador;
}
public void setNomeCoordenador(String nomeCoordenador) {
	this.nomeCoordenador = nomeCoordenador;
}
public int getIdCoordenadro() {
	return idCoordenadro;
}
public void setIdCoordenadro(int idCoordenadro) {
	this.idCoordenadro = idCoordenadro;
}
public String getCurso() {
	return curso;
}
public void setCurso(String curso) {
	this.curso = curso;
}

}
