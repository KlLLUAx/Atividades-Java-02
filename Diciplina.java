package br.com.fuctura.entidades;

public class Diciplina {
protected String nomeDiciplina;
protected String professor;
protected int codigoDiciplina;
protected String nivelAcademico;
public String getNomeDiciplina() {
	return nomeDiciplina;
}
public void setNomeDiciplina(String nomeDiciplina) {
	this.nomeDiciplina = nomeDiciplina;
}
public String getProfessor() {
	return professor;
}
public void setProfessor(String professor) {
	this.professor = professor;
}
public int getCodigoDiciplina() {
	return codigoDiciplina;
}
public void setCodigoDiciplina(int codigoDiciplina) {
	this.codigoDiciplina = codigoDiciplina;
}
public String getNivelAcademico() {
	return nivelAcademico;
}
public void setNivelAcademico(String nivelAcademico) {
	this.nivelAcademico = nivelAcademico;
}

}
